func detectAV() string {
	if runtime.GOOS != "windows" {
		return "N/A (Non-Windows)"
	}

	avList := []string{}
	knownAV := []string{
		"MsMpEng.exe",
		"AvastUI.exe",
		"avgui.exe",
		"ekrn.exe",
		"bdagent.exe",
	}

	for _, av := range knownAV {
		cmd := exec.Command("tasklist", "/FI", fmt.Sprintf("IMAGENAME eq %s", av))
		output, _ := cmd.Output()
		if strings.Contains(string(output), av) {
			avName := strings.TrimSuffix(av, ".exe")
			avList = append(avList, avName)
		}
	}

	if len(avList) == 0 {
		return "None Detected"
	}
	return strings.Join(avList, ", ")
}

