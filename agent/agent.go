func executeAtomicTest(tid string, testIndex int, test AtomicTest, inputArgs map[string]interface{}, taskID string) TestExecutionResult {
	result := TestExecutionResult{
		TechniqueID:   tid,
		TestNumber:    fmt.Sprintf("%d", testIndex), 
		TestName:      test.Name,
		Status:        "running",
		PrereqOutput:  []CommandOutput{},
		ErrorMessages: []string{},
		StartTime:     time.Now(),
	}
		// FIX: Prevent nil map panic
	if inputArgs == nil {
		inputArgs = make(map[string]interface{})
	}

	// FIX 3: Handle manual tests
	if strings.ToLower(test.Executor.Name) == "manual" {
		sendLiveLog(taskID, "╔═══════════════════════════════════════════════════════════════", false)
		sendLiveLog(taskID, "║ MANUAL TEST - Requires Human Interaction", false)
		sendLiveLog(taskID, "╚═══════════════════════════════════════════════════════════════", false)
		sendLiveLog(taskID, "", false)
		sendLiveLog(taskID, fmt.Sprintf("[TEST] %s", test.Name), false)
		sendLiveLog(taskID, fmt.Sprintf("[DESC] %s", test.Description), false)
		sendLiveLog(taskID, "", false)
		sendLiveLog(taskID, "[STEPS] Manual steps required:", false)
		
		steps := strings.Split(test.Executor.Command, "\n")
		for _, step := range steps {
			if strings.TrimSpace(step) != "" {
				sendLiveLog(taskID, "  → "+strings.TrimSpace(step), false)
			}
		}
		
		sendLiveLog(taskID, "", false)
		sendLiveLog(taskID, "[SKIP] This test cannot be automated - please perform manually", true)
		
		result.Status = "skipped"
		result.ErrorMessages = append(result.ErrorMessages, "Manual test requires operator intervention")
		result.EndTime = time.Now()
		result.Duration = result.EndTime.Sub(result.StartTime)
		sendTestResult(taskID, result)
		return result
	}

	// FIX 5: Check elevation requirement
	if test.Executor.ElevationRequired && !isElevated() {
		sendLiveLog(taskID, "[WARNING] ⚠ This test requires elevation (admin/root) but agent is not running elevated!", true)
		sendLiveLog(taskID, "[WARNING] Test may fail or produce incomplete results", false)
	}

	if !isPlatformSupported(test.SupportedPlatforms) {
		msg := fmt.Sprintf("[SKIP] Test not supported on platform: %s", runtime.GOOS)
		sendLiveLog(taskID, msg, false)
		result.Status = "skipped"
		result.ErrorMessages = append(result.ErrorMessages, msg)
		result.EndTime = time.Now()
		result.Duration = result.EndTime.Sub(result.StartTime)
		return result
	}

	absAtomicsPath, _ := filepath.Abs(AtomicsDir)
	finalArgs := make(map[string]string)
	finalArgs["PathToAtomicsFolder"] = absAtomicsPath

	for key, argDef := range test.InputArguments {
		if val, ok := inputArgs[key]; ok {
			finalArgs[key] = toString(val)
		} else {
			finalArgs[key] = toString(argDef.Default)
		}
	}

	if len(test.Dependencies) > 0 {
		sendLiveLog(taskID, fmt.Sprintf("[PREREQ] Found %d prerequisite(s)", len(test.Dependencies)), false)

		for idx, dep := range test.Dependencies {
			sendLiveLog(taskID, fmt.Sprintf("[PREREQ %d] %s", idx+1, dep.Description), false)

			if dep.PrereqCommand != "" {
				prereqCmd := replaceVariables(dep.PrereqCommand, finalArgs)
				sendLiveLog(taskID, fmt.Sprintf("[CHECK] %s", truncateString(prereqCmd, 150)), false)

				checkOutput := runCommandDetailed(prereqCmd, test.Executor.Name, taskID)
				result.PrereqOutput = append(result.PrereqOutput, checkOutput)

				isMet := checkOutput.ExitCode == 0
				outputStr := strings.ToLower(checkOutput.GetFullOutput())
				if strings.Contains(outputStr, "false") || strings.Contains(outputStr, "not found") {
					isMet = false
				}

				if isMet {
					sendLiveLog(taskID, "[OK] Prerequisite satisfied", false)
				} else {
					sendLiveLog(taskID, "[!] Prerequisite NOT MET - attempting to install...", false)

					if dep.GetPrereqCommand != "" {
						getCmd := replaceVariables(dep.GetPrereqCommand, finalArgs)
						sendLiveLog(taskID, fmt.Sprintf("[INSTALL] %s", truncateString(getCmd, 150)), false)

						installOutput := runCommandDetailed(getCmd, test.Executor.Name, taskID)
						result.PrereqOutput = append(result.PrereqOutput, installOutput)

						if installOutput.ExitCode == 0 {
							sendLiveLog(taskID, "[OK] Prerequisite installed", false)
						} else {
							sendLiveLog(taskID, "[!] Installation may have failed", false)
						}
					}
				}
			}
		}
	}

	if test.Executor.Command != "" {
		sendLiveLog(taskID, "[STEP] Executing main command...", false)
		command := replaceVariables(test.Executor.Command, finalArgs)

		sendLiveLog(taskID, fmt.Sprintf("[COMMAND] %s", truncateString(command, 300)), false)
		sendLiveLog(taskID, "", false)
		sendLiveLog(taskID, "[OUTPUT] ************************************************", false)

		result.MainOutput = runCommandDetailed(command, test.Executor.Name, taskID)

		sendLiveLog(taskID, "[OUTPUT] ************************************************", false)

		sendCommandOutput(taskID, tid, result.TestNumber, "main", result.MainOutput)

		if result.MainOutput.HasErrors() {
			result.Status = "failed"
		}
	}

	if test.Executor.CleanupCommand != "" {
		sendLiveLog(taskID, "[STEP] Running cleanup...", false)
		cleanupCmd := replaceVariables(test.Executor.CleanupCommand, finalArgs)
		result.CleanupOutput = runCommandDetailed(cleanupCmd, test.Executor.Name, taskID)
		sendCommandOutput(taskID, tid, result.TestNumber, "cleanup", result.CleanupOutput)
	}

	if result.Status != "failed" && result.Status != "skipped" {
		result.Status = "completed"
	}

	result.EndTime = time.Now()
	result.Duration = result.EndTime.Sub(result.StartTime)

	sendTestResult(taskID, result)

	return result
}

