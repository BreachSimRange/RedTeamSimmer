// TestExecutionResult stores detailed output from each test execution
type TestExecutionResult struct {
	TechniqueID     string
	TestNumber      string
	TestName        string
	Status          string
	PrereqOutput    []CommandOutput
	MainOutput      CommandOutput
	CleanupOutput   CommandOutput
	ErrorMessages   []string
	StartTime       time.Time
	EndTime         time.Time
	Duration        time.Duration
}

// CommandOutput stores output from a single command execution
type CommandOutput struct {
	Command       string
	StdoutLines   []string
	StderrLines   []string
	ExitCode      int
	ExecutionTime time.Duration
}

