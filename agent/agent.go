// findFileRecursive searches for a file by name under a directory tree
func findFileRecursive(rootDir, fileName string) (string, error) {
	target := strings.ToLower(fileName)
	var found string

	err := filepath.Walk(rootDir, func(path string, info os.FileInfo, walkErr error) error {
		if walkErr != nil {
			return nil // Skip paths we can't access
		}
		if info == nil || info.IsDir() {
			return nil
		}
		if strings.ToLower(info.Name()) == target {
			found = path
			return io.EOF // Stop early
		}
		return nil
	})

	if err != nil && err != io.EOF {
		return "", err
	}
	if found == "" {
		return "", fmt.Errorf("%s not found under %s", fileName, rootDir)
	}
	return found, nil
}
// findYamlForTechnique locates the atomic YAML for a technique ID on the agent's disk.
//
// It tries a strict, ordered list of candidate paths. Notably, it does NOT
// perform a filesystem walk or fuzzy filename search - the previous behaviour
// of falling back to "any yaml whose filename matches" silently loaded the
// wrong file for sub-technique requests (e.g. T1071.001 would find and execute
// tests from T1071.yaml instead).
//
// For sub-technique requests like T1071.001, the resolver will fall back to the
// parent yaml (T1071/T1071.yaml) if no nested sub-tech folder exists. Callers
// should detect this fallback by checking atomicTech.AttackTechnique after
// loading the yaml. The server-side resolver picks the correct test index in
// that case, so the agent can trust the test_index it was given.
func findYamlForTechnique(tid string) (string, error) {
	absAtomicsPath, _ := filepath.Abs(AtomicsDir)
	baseTid := strings.Split(tid, ".")[0]
	isSubTech := strings.Contains(tid, ".")

	var possiblePaths []string
	if isSubTech {
		// Sub-technique: prefer nested sub-tech folder, then parent-holding-sub,
		// then parent yaml (which may contain the sub-tech tests embedded).
		possiblePaths = []string{
			filepath.Join(absAtomicsPath, tid, tid+".yaml"),
			filepath.Join(absAtomicsPath, baseTid, tid+".yaml"),
			filepath.Join(absAtomicsPath, baseTid, baseTid+".yaml"),
		}
	} else {
		// Parent technique: single canonical location.
		possiblePaths = []string{
			filepath.Join(absAtomicsPath, tid, tid+".yaml"),
		}
	}

	for _, path := range possiblePaths {
		if _, err := os.Stat(path); err == nil {
			return path, nil
		}
	}

	return "", fmt.Errorf("YAML not found for %s (tried: %s)", tid, strings.Join(possiblePaths, " | "))
}
